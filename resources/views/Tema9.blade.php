									<h2>Explicar el proceso de diagramación de componentes de la arquitectura Cliente/Servidor</h2>
									<img src="/Img/Diagrama de componenetes 9.png" alt="LKImagen" width="950" height="500">
								</header>
								
                                <p>Es la representación de los contenidos que tendrá un producto digital, y las relaciones entre dichos contenidos. </p>
								<p>
								La diagramación es una herramienta que nos permite representar en forma gráfica los procesos de una empresa y observar las actividades en conjunto, sus relaciones y cualquier incompatibilidad. 
								</p>

						       <br>
								<p>
								Un diagrama de componentes, expande un contenedor individual para mostrar los componentes que contiene. Estos componentes deben asignarse a abstracciones reales (por ejemplo, una agrupación de códigos) en función de su código. A continuación, se muestra un ejemplo de un diagrama de componentes para el sistema ficticio de banca por Internet que muestra algunos (en lugar de todos) de los componentes de la aplicación API.
								</p>
						

								<br>
								<p>Dos controladores MVC Spring en Rest proporcionan puntos de acceso a la API de JSON/HTTTPS, y cada controlador utiliza posteriormente otros componentes para acceder a los datos de la base de datos y del sistema bancario en el mainframe.</p>
								



