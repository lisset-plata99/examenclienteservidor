									<h2>Reconocer los conceptos de sistemas de información</h2>
									<img src="/Img/01.png" alt="LKImagen" width="500" height="200">
								</header>

								<p>Se refiere a un conjunto ordenado de mecanismos que tienen como fin la administración de datos y de información, de manera que puedan ser recuperados y procesados fácil y rápidamente.
									Un sistema de información está conformado por una serie de datos vinculados entre sí para conseguir un objetivo común.
									Un sistema de información tiene como principal objetivo la gestión, y administración de los datos e información que lo componen, lo importante es poder recuperar siempre esos datos, y que además se tenga un fácil acceso a ellos con total seguridad.
								</p>

								<p>Un sistema de información está conformado por una serie de datos vinculados entre sí para conseguir un objetivo común.
								</p>

								<p>Un sistema de información tiene como principal objetivo la gestión, y administración de los datos e información que lo componen, lo importante es poder recuperar siempre esos datos, y que además se tenga un fácil acceso a ellos con total seguridad.
								</p>

								<br>

								<header>
									<h2>Cómo funciona un sistema de información</h2>
									<img src="/Img/01.1.png" alt="LKImagen" width="500" height="200">
								</header>

								<p>Esta es la metodología habitual:
								</p>

								<p>La entrada de la información. En este paso se da entrada a la información de manera automática o manual, dependiendo de la técnica que se utilice para incluir los datos. Las manuales las introduce directamente el usuario, las automáticas se gestionan a través de información recibida o proveniente de otro tipo de áreas y módulos. Las formas de entrada más habituales de registros y datos son las siguientes: códigos de barra, el teclado de un ordenador, cajas registradoras, sistemas de voz, un escáner, entre otros.
								</p>

								<p>El procesamiento de la información registrada. En el sistema existen una serie de operaciones y acciones que previamente se han configurado y establecido. Una vez que se ha producido la entrada de la información se procede a transformar esos datos en información requerida para la toma de decisiones, valoración, investigación y análisis para llevar a cabo un balance o visión general en función de los contenidos que resultan de este proceso.
								</p>

								<p>El almacenamiento de la información. Esta opción permite que la información quede registrada en el sistema, en un ordenador, por ejemplo. De esta forma, si quiere revisarse o acceder a ella cuando se requiera, siempre estará almacenada y accesible para las tareas que se requieran. En los ordenadores suele almacenarse en discos duros interno, o extraíbles, o incluso en los CDs habituales, aunque su utilización suele ser menor, y en muchos ordenadores se ha dejado de incluir el soporte para ellos.
								</p>

								<p>La salida de la información. En este caso la información que está incluida en un determinado soporte y previamente procesada, facilita a un usuario a que tenga acceso a ella y pueda sacarla a través de diferentes dispositivos como: usb, impresoras, sistemas de voz, entre otros.
								</p>

								<header>
									<h2>Los sistemas de información más destacados</h2>
								</header>

								<p>Estos son los principales sistemas que existen:
								</p>

								<p>Sistemas de información en marketing y comunicación. Su principal objetivo es la venta y promoción de artículos. La creación de información y datos para que se puedan dar a conocer este tipo de artículos y sean de interés para el público.
								</p>

								<p>El sistema de información administrativa (MIS). Es uno de los más importantes, y se basa en proporcionar la información más destacada y relevante que necesita la empresa para la toma de decisiones, y valoración de datos para llevar a cabo un plan de acción posterior en base a esta recogida de documentos.
								</p>

								<p>El sistema de procesamiento de transacciones (TPS). En este caso los datos aquí se incluyen son los referidos a las actividades comerciales de la empresa.
								</p>

								<p>El sistema de apoyo a ejecutivos (EIS). Destinado especialmente a aportar información y datos relevantes para la toma de decisiones estratégicas en concreto.
								</p>

								<p>El sistema para la toma de decisiones en grupo (GDSS). La información tiene como objetivo ser compartida entre los componentes del grupo para que se faciliten las tareas de trabajos y la cooperación entre todos.
								</p>

								<p>El sistema de información para expertos (EDSS). En este caso lo que se trata de conseguir es compartir datos de interés entre determinados segmentos y especialistas para valorar esa información destinada a ellos.
								</p>

