									<h2>Definir el los conceptos de cliente y servidor</h2>
									<img src="/Img/03.png" alt="LKImagen" width="500" height="200">
								</header>
								<p>Cliente:
								</p>
								<p>Se llama cliente al dispositivo que requiere ciertos servicios a un servidor.
                                 Un sistema que utiliza servicios remotos de un servidor. Algunos clientes tienen una capacidad de almacenamiento en disco limitada o, tal vez, nula. Estos clientes dependen de sistemas de archivos remotos de un servidor para funcionar. Los sistemas sin disco y los sistemas de dispositivos son ejemplos de este tipo de clientes.
                                 El cliente, en este marco, realiza peticiones de servicios al servidor, que se encarga de satisfacer dichos requerimientos.
                                 Otros clientes quizás utilicen los servicios remotos (como el software de instalación) de un servidor. Sin embargo, no dependen de un servidor para funcionar. Un sistema independiente es un buen ejemplo de este tipo de clientes. 

								</p>

								

								<br>


								<p>Servidor:
								</p>			

								<p>Un sistema que proporciona servicios a otros sistemas en su red. Hay servidores de archivos, servidores de inicio, servidores web, servidores de bases de datos, servidores de licencias, servidores de impresión, servidores de instalación, servidores de dispositivos e, incluso, servidores para aplicaciones específicas. En este capítulo, se utiliza el término servidor para denominar un sistema que proporciona servicios de inicio y sistemas de archivos a otros sistemas de la red.
                                  La idea de servidor, por su parte, alude al equipo que brinda servicios a las computadoras (ordenadores) que se hallan conectadas con él mediante una red.
                                  Un servidor es una aplicación que ofrece un servicio a usuarios de Internet; un cliente es el que pide ese servicio. Una aplicación consta de una parte de servidor y una de cliente, que se pueden ejecutar en el mismo o en diferentes sistemas.
                                  El concepto de cliente servidor, o cliente-servidor, refiere por lo tanto a un modelo de comunicación que vincula a varios dispositivos informáticos a través de una red. 

								</p>

